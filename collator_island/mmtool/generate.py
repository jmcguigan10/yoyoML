def _iter_pipelines(raw: Dict[str, Any]) -> Iterable[Dict[str, Any]]:
    pipes = raw.get("pipelines", [])
    if not isinstance(pipes, list):
        raise ConfigError("'pipelines' must be a list")
    for p in pipes:
        if not isinstance(p, dict):
            continue
        if not isinstance(p.get("name"), str) or not isinstance(p.get("kind"), str):
            continue
        yield p


class ProjectGenerator:
    """Generate pipeline folders from a project config.

    This is intentionally small and boring: load config -> plan files -> assemble snippets.
    """

    def __init__(self, *, txt_store: Path, db_path: Path) -> None:
        self.txt_store = Path(txt_store)
        self.db_path = Path(db_path)

    def _ensure_db(self) -> SnippetDB:
        # Auto-refresh the registry if snippets changed.
        needs_rebuild = not self.db_path.exists()
        if not needs_rebuild and self.txt_store.exists():
            newest = max(
                (p.stat().st_mtime for p in self.txt_store.rglob("*.txt")),
                default=0.0,
            )
            needs_rebuild = newest > self.db_path.stat().st_mtime

        if needs_rebuild:
            build_db(self.txt_store, self.db_path)
        return SnippetDB(db_path=self.db_path, txt_root=self.txt_store)

    def generate(
        self,
        *,
        project_file: str | Path,
        out_dir: str | Path,
        force: bool = False,
        pipeline_config_format: str = "toml",
    ) -> Tuple[Path, int]:
        """Generate pipeline folders.

        Args:
            project_file: Project config (.toml or .yaml/.yml)
            out_dir: Output directory
            force: If True, overwrite existing pipeline directories
            pipeline_config_format: "toml" (recommended) or "yaml"

        Returns:
            (out_dir, num_pipelines_generated)
        """
        project_file = Path(project_file)
        out_dir = Path(out_dir)

        raw = load_config(project_file)
        out_dir.mkdir(parents=True, exist_ok=True)

        db = self._ensure_db()

        count = 0
        for p in _iter_pipelines(raw):
            name = str(p["name"])
            kind = str(p["kind"]).lower()
            enabled = bool(p.get("enabled", True))
            if not enabled:
                continue

            file_templates = get_templates_for_pipeline(kind, p)
            if not file_templates:
                raise ConfigError(f"No templates registered for pipeline kind '{kind}'.")

            pipe_dir = out_dir / name
            if pipe_dir.exists():
                if not force:
                    raise ConfigError(
                        f"Output already exists for pipeline '{name}': {pipe_dir}. Use --force to overwrite."
                    )
                shutil.rmtree(pipe_dir)

            # Make folders
            (pipe_dir / "src").mkdir(parents=True, exist_ok=True)
            (pipe_dir / "config").mkdir(parents=True, exist_ok=True)

            # Write pipeline config in a sane format.
            if pipeline_config_format not in {"toml", "yaml"}:
                raise ConfigError("pipeline_config_format must be 'toml' or 'yaml'")
            cfg_name = f"pipeline.{pipeline_config_format}"
            (pipe_dir / "config" / cfg_name).write_text(
                dump_config(p, fmt=pipeline_config_format),
                encoding="utf-8",
            )

            # Generated python files
            for ft in file_templates:
                out_path = pipe_dir / ft.out_relpath
                module_doc = f"Generated by mmtool prototype v2 for pipeline '{name}'."

                if ft.out_relpath == "src/criterion.py":
                    _write_criterion(
                        db=db,
                        pipe_cfg=p,
                        out_path=out_path,
                        module_docstring=module_doc,
                    )
                else:
                    assemble_file(
                        db=db,
                        snippet_keys=ft.snippet_keys,
                        out_path=out_path,
                        module_docstring=module_doc,
                    )

            # Small package init
            (pipe_dir / "src" / "__init__.py").write_text(
                """# Generated pipeline package
from .model import *  # noqa
from .criterion import *  # noqa
from .config import *  # noqa
from .data import *  # noqa
""",
                encoding="utf-8",
            )

            (pipe_dir / "README.md").write_text(
                _pipeline_readme(name=name, cfg_name=cfg_name),
                encoding="utf-8",
            )

            count += 1

        return out_dir, count


def generate(
    *,
    project_file: str | Path,
    out_dir: str | Path,
    txt_store: str | Path,
    db_path: str | Path,
    force: bool = False,
    pipeline_config_format: str = "toml",
) -> Tuple[Path, int]:
    """Backwards-friendly wrapper for the CLI."""
    gen = ProjectGenerator(txt_store=Path(txt_store), db_path=Path(db_path))
    return gen.generate(
        project_file=Path(project_file),
        out_dir=Path(out_dir),
        force=force,
        pipeline_config_format=pipeline_config_format,
    )


def _pipeline_readme(*, name: str, cfg_name: str) -> str:
    return f"""# {name}

This folder was generated by the **mmtool prototype v2**.

## What you get
- `config/{cfg_name}`: your pipeline config
- `src/model.py`: model code (architecture + forward only)
- `src/criterion.py`: loss/criterion code (separate classes)
- `src/config.py`: dataclass-based config loader
- `src/data.py`: dataset + dataloader helpers
- `src/run_demo.py`: quick smoke-test
- `src/train.py`: minimal training skeleton

## Try it
```bash
python src/run_demo.py
python src/train.py
```
"""


