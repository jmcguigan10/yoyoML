# --- Criterion assembly helpers ---

LOSS_KEY_MAP = {
    "cross_entropy": "ce",
    "ce": "ce",
    "bce_with_logits": "bce",
    "bce": "bce",
    "mse": "mse",
    "l2": "mse",
}

LOSS_CLASS = {
    "ce": "CrossEntropyCriterion",
    "bce": "BCEWithLogitsCriterion",
    "mse": "MSECriterion",
}


def _canonical_loss(name: str) -> str:
    key = LOSS_KEY_MAP.get(name.lower())
    if not key:
        raise ConfigError(f"Unsupported criterion kind '{name}'.")
    return key


def _build_factory_single(loss_key: str) -> str:
    cls = LOSS_CLASS[loss_key]
    alias_map = {k: v for k, v in LOSS_KEY_MAP.items() if v == loss_key}
    lines = [
        "def make_single_task_criterion(kind: str, *, label_smoothing: float = 0.0, pos_weight: Optional[float] = None) -> Criterion:",
        f"    _canon = {alias_map!r}.get(kind.lower())",
        f"    if _canon != '{loss_key}':",
        "        raise ValueError(f\"Unsupported criterion kind '{kind}' for this pipeline.\")",
    ]
    if loss_key == "ce":
        lines.append(f"    return {cls}(label_smoothing=label_smoothing)")
    elif loss_key == "bce":
        lines.append(f"    return {cls}(pos_weight=pos_weight)")
    else:
        lines.append(f"    return {cls}()")
    return "\n".join(lines) + "\n"


def _build_factory_mtl(loss_keys: set[str]) -> str:
    alias_map = {k: v for k, v in LOSS_KEY_MAP.items() if v in loss_keys}
    lines = [
        "def make_single_task_criterion(kind: str, *, label_smoothing: float = 0.0, pos_weight: Optional[float] = None) -> Criterion:",
        f"    _canon = {alias_map!r}.get(kind.lower())",
    ]
    for k in sorted(loss_keys):
        cls = LOSS_CLASS[k]
        lines.append(f"    if _canon == '{k}':")
        if k == "ce":
            lines.append(f"        return {cls}(label_smoothing=label_smoothing)")
        elif k == "bce":
            lines.append(f"        return {cls}(pos_weight=pos_weight)")
        else:
            lines.append(f"        return {cls}()")
    allowed = ",".join(sorted(loss_keys))
    lines.append(
        f"    raise ValueError(f\"Unsupported criterion kind '{{kind}}'; allowed: {allowed}\")"
    )
    return "\n".join(lines) + "\n"


def _criterion_snippets(db: SnippetDB, pipe_cfg: dict) -> list[Snippet]:
    def snip(name: str) -> Snippet:
        return Snippet(name=name, code=db.get_code(name))

    common = [
        snip("common.future_annotations"),
        snip("common.imports_torch"),
        snip("criteria.base"),
    ]

    kind = pipe_cfg["kind"].lower()
    crit_cfg = pipe_cfg.get("criterion", {}) or {}

    if kind in {"cnn", "mlp"}:
        loss_key = _canonical_loss(str(crit_cfg.get("kind", "")))
        class_snip = snip(f"criteria.{loss_key}")
        factory = Snippet(name="criteria.factory", code=_build_factory_single(loss_key))
        return common + [class_snip, factory]

    if kind == "mtl":
        task_losses = crit_cfg.get("task_losses", {})
        if not isinstance(task_losses, dict) or not task_losses:
            raise ConfigError(
                f"[{pipe_cfg.get('name')}] MTL pipelines need non-empty criterion.task_losses"
            )
        loss_keys = {_canonical_loss(k) for k in task_losses.values()}
        class_snips = [snip(f"criteria.{k}") for k in sorted(loss_keys)]
        factory = Snippet(name="criteria.factory", code=_build_factory_mtl(loss_keys))
        return common + class_snips + [factory, snip("criteria.mtl_weighted")]

    raise ConfigError(f"Unknown pipeline kind: {kind}")


def _write_criterion(
    *, db: SnippetDB, pipe_cfg: dict, out_path: Path, module_docstring: str
) -> None:
    snippets = _criterion_snippets(db, pipe_cfg)
    assembler = SnippetAssembler(snippets, module_docstring=module_docstring)
    assembler.write(out_path)
